    async def _ensure_model(self):
        """Lazy initialise and inject model service from app state."""
        if self.model_service is not None:
            return

        # Import here to avoid circular imports
        try:
            from app.main import model_service  # type: ignore
            self.model_service = model_service
        except Exception:
            pass

        if self.model_service is None:
            try:
                from app.services.model_service import ModelService  # type: ignore
                self.model_service = ModelService()
                await self.model_service.initialize()
            except Exception as exc:
                logger.error(f"Could not initialise ModelService: {exc}")

