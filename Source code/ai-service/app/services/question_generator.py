    async def _gen_mcq(self, section: str, topic: str, difficulty: str, profile_ctx: str, company: str, seed: str, idx: int, category: str = "") -> dict:
        prompt = self._mcq_prompt(section, topic, difficulty, profile_ctx, company, seed, idx, category)
        for attempt in range(3):
            try:
                raw = await self._llm(prompt, max_tokens=800, temperature=0.85 + (attempt * 0.05))
                data = self._parse_json(raw)
                if data and "question" in data and "options" in data and len(data["options"]) == 4:
                    seed8 = self._trunc(seed, 8)
                    return {
                        "id": f"mcq_{seed8}_{idx}",
                        "type": "mcq",
                        "section": section,
                        "topic": topic,
                        "difficulty": difficulty,
                        "question": data["question"],
                        "options": data["options"],
                        "correct": int(data.get("correct") if data.get("correct") is not None else 0) % 4,
                        "explanation": data.get("explanation", ""),
                        "conceptsTested": data.get("conceptsTested", [topic]),
                        "companyAskedIn": data.get("companyAskedIn", company.upper() or "Top Tech firms"),
                        "aiGenerated": True,
                        "uniqueSeed": seed8,
                    }
            except Exception as exc:
                if attempt == 2:
                    logger.debug(f"Last MCQ attempt raw output: {raw if 'raw' in locals() else 'N/A'}")
                logger.warning(f"MCQ generation failed for {section}/{topic} (attempt {attempt+1}): {exc}")
                
        # Return None instead of raising to allow section to contain other valid questions
        return None

