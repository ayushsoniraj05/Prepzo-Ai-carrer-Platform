    async def _llm(self, prompt: str, max_tokens: int = 1000, temperature: float = 0.85) -> str:
        """Call the Ollama model with concurrency control; raise if unavailable."""
        await self._ensure_model()
        if self.model_service is None or not self.model_service.has_model:
            raise RuntimeError("Ollama model not available. Ensure Ollama is running.")
        
        async with self._semaphore:
            return await self.model_service.generate(prompt, max_tokens=max_tokens, temperature=temperature)

