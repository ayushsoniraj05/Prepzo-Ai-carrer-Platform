    # ──────────────────────────────────────────────────────────────────────────

    def _question_type(self, section: str, difficulty: str, idx: int) -> str:
        sl = section.lower()
        if sl in ("coding", "dsa", "problem solving", "algorithms"):
            if difficulty in ("hard", "advanced"):
                return "coding"
            if idx % 3 == 0:
                return "coding"
        if idx % 5 == 0 and sl not in ("aptitude",):
            return "short_answer"
        return "mcq"

