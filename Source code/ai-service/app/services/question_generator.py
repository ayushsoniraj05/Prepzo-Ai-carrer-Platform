    def _difficulty_counts(self, n: int, dist: Dict[str, float]) -> Dict[str, int]:
        safe_n = max(1, int(n) if n is not None else 1)
        counts: Dict[str, int] = {d: max(0, int(safe_n * float(dist.get(d) if dist.get(d) is not None else 0))) for d in self.DIFFICULTY_ORDER}
        deficit = safe_n - sum(counts.values())
        counts["hard"] = max(0, counts["hard"] + deficit) # Prioritize hard for deficits
        return counts

    @staticmethod
    def _parse_json(raw: str) -> Optional[Dict[str, Any]]:
        """Robustly extract first JSON object from model output."""
        if not raw:
            return None
            
        # Try to find JSON block in markdown
        for pattern in (
            r"```(?:json)?\s*([\s\S]*?)\s*```",
            r"(\{[\s\S]*\})",
        ):
            m = re.search(pattern, raw)
            if m:
                candidate = m.group(1).strip()
                try:
                    # Clean up common model hallucinations in JSON
                    # Replace single quotes with double quotes (rough heuristic)
                    cleaned = candidate.replace("'", '"')
                    # Remove trailing commas before closing braces/brackets
                    cleaned = re.sub(r',\s*\}', '}', cleaned)
                    cleaned = re.sub(r',\s*\]', ']', cleaned)
                    # Handle unescaped newlines in strings (very common in 1B models)
                    cleaned = re.sub(r'(?<!\\)\n', ' ', cleaned)
                    return json.loads(cleaned)
                except json.JSONDecodeError:
                    # Try raw candidate if cleaning failed
                    try:
                        return json.loads(candidate)
                    except json.JSONDecodeError:
                        pass

        # Last-ditch: find the outermost { }
        start = raw.find("{")
        end = raw.rfind("}") + 1
        if start != -1 and end > start:
            try:
                candidate = str(raw)[start:end]
                # Same cleaning for last-ditch
                cleaned = re.sub(r'(?<!\\)\n', ' ', candidate)
                cleaned = re.sub(r',\s*\}', '}', cleaned)
                return json.loads(cleaned)
            except json.JSONDecodeError:
                pass
        return None

