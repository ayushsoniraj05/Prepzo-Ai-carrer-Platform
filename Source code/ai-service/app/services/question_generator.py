    # ──────────────────────────────────────────────────────────────────────────

    async def _generate_section(self, section: str, profile: dict, num_q: int, dist: dict, seed: str, stream_cat: str, company_pattern: dict = None, company: str = "", category: str = "") -> list:
        topics: List[str] = self._pick_topics(stream_cat, section, profile)
        profile_ctx = self._profile_context(profile)
        
        # If no specific company requested, we will rotate between top-tier firms in the prompt
        top_firms = ["Amazon", "Google", "Microsoft", "Adobe", "Uber", "Zomato", "Goldman Sachs", "Intuit", "Directi", "Slice"]
        
        counts: Dict[str, int] = self._difficulty_counts(num_q, dist)
        tasks = []
        q_idx = 0
        for difficulty in self.DIFFICULTY_ORDER:
            count_for_diff: int = counts.get(difficulty, 0)
            for _i in range(count_for_diff):
                t_idx: int = q_idx % max(1, len(topics))  # pyre-ignore[58]
                topic: str = topics[t_idx]
                qtype = self._question_type(section, difficulty, q_idx)
                target_company = company if company else random.choice(top_firms)

                if qtype == "coding":
                    tasks.append(self._gen_coding(section, topic, difficulty, profile_ctx, target_company, seed, q_idx, category))
                elif qtype == "short_answer":
                    tasks.append(self._gen_short_answer(section, topic, difficulty, profile_ctx, target_company, seed, q_idx, category))
                else:
                    tasks.append(self._gen_mcq(section, topic, difficulty, profile_ctx, target_company, seed, q_idx, category))
                q_idx += 1

        # Generate questions in small batches (chunks) to prevent timeouts on Render
        results = []
        chunk_size = 5
        for i in range(0, len(tasks), chunk_size):
            chunk = tasks[i : i + chunk_size]
            batch_results = await asyncio.gather(*chunk, return_exceptions=True)
            results.extend(batch_results)
            # Yield control back to ensure event loop stays alive
            await asyncio.sleep(0.2)
        
        questions: List[Dict[str, Any]] = []
        for i, res in enumerate(results):
            if isinstance(res, Exception) or res is None:
                # Slot-based recovery: if AI failed this specific question, inject a safety one immediately
                logger.warning(f"  → Question slot {i} failed in {section}. Injecting safety fallback.")
                
                safety_qs = self._get_safety_questions(section, 1, seed)
                if safety_qs:
                    questions.append(safety_qs[0])
                continue
                
            if isinstance(res, dict):
                questions.append(res)

        # Final check - ensure we at least have 3 questions per section
        if len(questions) < 3:
            logger.warning(f"  → AI failed to produce minimum questions for {section}. Filling from safety net.")
            missing = max(0, 3 - len(questions))
            if missing > 0:
                safety = self._get_safety_questions(section, missing, seed)
                questions.extend(safety)
            
        return questions

