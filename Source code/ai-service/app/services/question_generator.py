    async def get_next_question(
        self,
        student_profile: Dict[str, Any],
        section: str,
        performance: Dict[str, Any],
        answered_ids: List[str],
    ) -> Dict[str, Any]:
        difficulty = await self.adjust_difficulty(performance, performance.get("currentDifficulty", "medium"))
        stream_cat = self._stream_category(student_profile)
        profile = student_profile or {}
        topics = self._pick_topics(stream_cat, section, profile)
        seed = self._unique_seed(student_profile)
        idx = len(answered_ids)
        topic = topics[idx % len(topics)]
        qtype = self._question_type(section, difficulty, idx)
        profile_ctx = self._profile_context(student_profile)

        if qtype == "coding":
            return await self._gen_coding(section, topic, difficulty, profile_ctx, "", seed, idx)
        elif qtype == "short_answer":
            return await self._gen_short_answer(section, topic, difficulty, profile_ctx, "", seed, idx)
        else:
            return await self._gen_mcq(section, topic, difficulty, profile_ctx, "", seed, idx)

    def _trunc(self, text: str, length: int) -> str:
        """Truncate string and remove special chars."""
        clean = "".join(e for e in str(text) if e.isalnum())
        return clean[:length]

    def _unique_seed(self, profile: Dict[str, Any]) -> str:
        """Generate a stable but unique seed for the student/attempt."""
        uid = str(profile.get("id") or profile.get("userId") or "anon")
        now = datetime.utcnow().strftime("%Y%m%d%H") # Hourly stable seed
        raw = f"{uid}-{now}-{random.random()}"
        return hashlib.md5(raw.encode()).hexdigest()

