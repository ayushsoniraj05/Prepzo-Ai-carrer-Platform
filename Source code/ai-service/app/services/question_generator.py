    async def generate_test(
        self,
        student_profile: Dict[str, Any],
        test_config: Optional[Dict[str, Any]] = None,
    ) -> Dict[str, Any]:
        """
        Generate a complete, fully AI-driven test.
        100% unique per student per attempt.
        """
        cfg = test_config or {}
        num_q_raw = cfg.get("questionsPerSection")
        time_per_raw = cfg.get("timePerSection")

        seed = self._unique_seed(student_profile)
        seed8 = self._trunc(seed, 8)

        logger.info(
            f"[QuestionGenerator] Generating test for '{student_profile.get('name')}' | "
            f"seed={seed8} | role={student_profile.get('targetRole')}"
        )

        stream_cat = self._stream_category(student_profile)
        company = (cfg.get("company") or "").lower().strip()
        company_pattern = self.COMPANY_PATTERNS.get(company)

        # Handle separated test modes (Field vs Skills)
        test_mode = (cfg.get("testMode") or "combined").lower()
        
        final_sections = []
        if test_mode == "skills":
            # ONLY use skills selected by the student
            skill_ratings = student_profile.get("skillRatings", {})
            user_skills = student_profile.get("knownTechnologies", [])
            available_skills = set(list(skill_ratings.keys()) + user_skills)
            
            final_sections = list(available_skills)
            if not final_sections:
                logger.warning("No skills provided for skills test, falling back to field mode")
                test_mode = "field"

        if test_mode == "field" or test_mode == "combined":
            initial_sections = ["Aptitude"]
            stream_cores = list(self.STREAM_SECTIONS.get(stream_cat, {}).keys())
            initial_sections.extend([s for s in stream_cores if s != "Aptitude"])
            
            # For 'field', we don't mix skills. For 'combined', we do (legacy behavior).
            if test_mode == "combined":
                skill_ratings = student_profile.get("skillRatings", {})
                high_skills = [k for k, v in skill_ratings.items() if v >= 6]
                initial_sections.extend(high_skills)
            
            seen = set()
            for s in initial_sections:
                if s.lower() not in seen:
                    final_sections.append(s)
                    seen.add(s.lower())
        
        sections = cfg.get("sections") or final_sections
        # Ensure Aptitude is included if not already present
        if test_mode != "skills" and "Aptitude" not in sections and len(sections) > 0:
            sections.insert(0, "Aptitude")

        # Difficulty distribution based on CGPA and ratings if available
        # If CGPA > 8 or high skill ratings, increase Hard/Advanced percent
        dist = {"easy": 0.15, "medium": 0.40, "hard": 0.30, "advanced": 0.15}
        
        num_q = max(3, min(int(num_q_raw) if num_q_raw is not None else 20, 25))
        time_per_section = int(time_per_raw) if time_per_raw is not None else 1200   # 20 min default

        test_sections = []
        for sec_name in sections:
            logger.info(f"  → Generating section: {sec_name}")
            try:
                qs = await self._generate_section(
                    section=sec_name,
                    profile=student_profile,
                    num_q=num_q,
                    dist=dist,
                    seed=seed,
                    stream_cat=stream_cat,
                    company_pattern=company_pattern,
                    company=company,
                    category=cfg.get("category", "")
                )
                if qs:
                    test_sections.append({
                        "id":        f"sec_{sec_name.lower().replace(' ', '_')}_{self._trunc(seed, 6)}",
                        "name":      sec_name,
                        "questions": qs,
                        "timeLimit": time_per_section,
                    })
                else:
                    logger.warning(f"Section {sec_name} generated with no questions.")
            except Exception as e:
                logger.error(f"Failed to generate section {sec_name}: {e}")
                continue

        total_questions: int = sum(len(s["questions"]) for s in test_sections)  # pyre-ignore[6]
        if total_questions == 0:
            raise ValueError("AI generation failed to produce any valid questions. Please try again or switch model.")
        
        sec_count: int = len(test_sections)
        tt_raw = cfg.get("totalTime")
        total_time: int = int(tt_raw) if tt_raw is not None else (sec_count * time_per_section)

        result = {
            "testId":          f"test_{seed}",
            "studentId":       student_profile.get("id", "unknown"),
            "studentName":     student_profile.get("name", "Student"),
            "targetRole":      student_profile.get("targetRole", "Software Engineer"),
            "field":           student_profile.get("fieldOfStudy", "Generic"),
            "degree":          student_profile.get("degree", "B.Tech"),
            "generatedAt":     datetime.utcnow().isoformat(),
            "company":         company.upper() if company else "TOP-TIER MIX",
            "sections":        test_sections,
            "totalQuestions":  total_questions,
            "totalTime":       total_time,
            "adaptiveDifficulty": True,
            "uniqueToStudent": True,
            "aiGenerated":     True,
        }

        logger.info(
            f"[QuestionGenerator] ✅ Test generated: {total_questions} questions "
            f"across {sec_count} sections (seed={seed8})"
        )
        return result

    # ──────────────────────────────────────────────────────────────────────────
    # Public API — adaptive difficulty
    # ──────────────────────────────────────────────────────────────────────────

    async def adjust_difficulty(
        self,
        performance: Dict[str, Any],
        current_difficulty: str,
    ) -> str:
        levels = self.DIFFICULTY_ORDER
        idx = levels.index(current_difficulty) if current_difficulty in levels else 1

        correct = performance.get("correctAnswers", 0)
        total = max(performance.get("totalAnswers", 1), 1)
        accuracy = correct / total
        streak_correct = performance.get("consecutiveCorrect", 0)
        streak_wrong = performance.get("consecutiveWrong", 0)

        if streak_correct >= 3 and accuracy >= 0.75:
            idx = min(idx + 1, len(levels) - 1)
        elif streak_wrong >= 2 or accuracy < 0.35:
            idx = max(idx - 1, 0)

        return levels[idx]

